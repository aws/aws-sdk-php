    /**
     * @param string $word
     * @return bool
     */
    public static function isReserved($word)
    {
        return in_array(strtoupper($word), (new ReflectionClass(__CLASS__))->getConstants());
    }

    /**
     * @param string $word
     * @return bool
     */
    public static function notReserved($word)
    {
        return ! static::isReserved($word);
    }
